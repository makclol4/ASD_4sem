"""
Задача о раскраске графа заключается в нахождении минимального количества цветов, 
необходимых для раскраски вершин графа, так чтобы смежные вершины имели разные цвета. 

Алгоритм раскраски графа, который мы будем использовать, называется жадным алгоритмом.
 Он заключается в выборе вершин по одной и присваивании им минимального доступного цвета,
 который еще не назначен соседним вершинам. 

Для реализации данного алгоритма на Python нам понадобятся следующие инструменты:

1. Структура данных графа - можем использовать словарь с ключами, соответствующими 
вершинам, и значениями, представленными списками смежных вершин.

2. Список цветов - для хранения цветов, назначаемых вершинам.

3. Функция `is_color_valid` - для проверки, можно ли назначить этот цвет вершине 
на основе цветов ее соседей.

4. Жадный алгоритм раскраски графа - для назначения цветов вершинам по одной.
"""

# определяем структуру графа, используя словарь
graph = {
        'A': ['B', 'C'],
        'B': ['A', 'C', 'D'],
        'C': ['A', 'B', 'D', 'E'],
        'D': ['B', 'C', 'E', 'F'],
        'E': ['C', 'D'],
        'F': ['D']
        }

# определяем количество цветов, используемых в раскраске
num_colors = 3

# инициализируем список цветов, присваивая каждой вершине пустой цвет
vertex_colors = {}

# определяем функцию для проверки, можно ли назначить этот цвет вершине на основе цветов ее соседей
def is_color_valid(vertex, color):
    # проходим по списку смежных вершин
    for neighbor in graph[vertex]:
        # если соседняя вершина уже имеет такой цвет, результат ложный
        if vertex_colors.get(neighbor) == color:
            return False
    # если все соседи имеют разные цвета, результат истинный
    return True

# жадный алгоритм раскраски графа
for vertex in graph:
    # проходим по всем цветам и пытаемся найти минимальный доступный цвет
    for color in range(num_colors):
        if is_color_valid(vertex, color):
            vertex_colors[vertex] = color
            break

# выводим итоговую раскраску вершин графа
for vertex, color in vertex_colors.items():
    print(f"{vertex}: {color}")